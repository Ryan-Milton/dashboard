  apiKey: process.env.REACT_APP_FIREBASE_API_KEY,
  authDomain: "dashboard-233218.firebaseapp.com",
  projectId: "dashboard-233218",
  storageBucket: "dashboard-233218.appspot.com",
  messagingSenderId: "361843544822",
